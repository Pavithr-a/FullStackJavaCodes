package Day15;

import java.util.TreeSet;

public class TreeSetDemo {
	public static void main(String[] args) {
		TreeSet<String> treeSet=new TreeSet<String>();
		treeSet.add("Orange");
		treeSet.add("Blue");
		treeSet.add("Red");
		treeSet.add("Purple");
		treeSet.add("Pink");
		
		System.out.println("TreeSet: "+treeSet);
	}

}
